package day01; //위치 정보

//샘플 프로그램
public class Welcome {//파일마다 무조건 있어야하는 기본 단위 (집)
 public static void main(String[] args) {//코드의 기본 실행 단위(메소드 = 방)
  
	 //실제 프로그램이 실행코드 코드
	 System.out.println("자바에 오신걸 환영합니다!"); //화면에 글자를 출력하는 명령(표준 출력 명령)
 
	 System.out.println("1 + 2");//따옴표가 있으면 글자로 생각하라는 뜻
	 System.out.println(1 + 2);//따옴표가 없으면 값으로 생각하라는 뜻(필요 시 계산 허용)
 }
}
 